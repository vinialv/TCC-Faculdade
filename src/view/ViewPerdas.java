    public void salvar() {

        BLDatas bl = new BLDatas();
        BLMascaras con = new BLMascaras();

        modelPerdas.setDescProduto(String.valueOf(comboProduto.getSelectedItem()));
        modelPerdas.setDescEtapa(String.valueOf(comboEtapa.getSelectedItem()));
        modelPerdas.setDescRecurso(String.valueOf(comboRecurso.getSelectedItem()));
        try {
            modelPerdas.setDataPerda(bl.converterDataParaDateUS(bl.converterDataStringParaDate(dataPerdas.getText())));
        } catch (Exception ex) {
            Logger.getLogger(ViewPerdas.class.getName()).log(Level.SEVERE, null, ex);
        }
        modelPerdas.setMotivo(String.valueOf(comboMotivo.getSelectedItem()));
        modelPerdas.setPesoPerda(Double.parseDouble(con.converterVirgulaParaPonto(fieldPeso.getText())));
        modelPerdas.setOperador(String.valueOf(comboOperador.getSelectedItem()));
        try {
            SimpleDateFormat horaMinutos = new SimpleDateFormat("HH:mm");
            Date data = horaMinutos.parse(horaPerdas.getText());
            Time time = new Time(data.getTime());
            modelPerdas.setHoraPerda(time);
        } catch (ParseException ex) {
            Logger.getLogger(ViewPerdas.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (salvarAlterar.equals("salvar")) {
            if (controllerPerdas.salvarCadastroPerdas(modelPerdas) > 0) {
                JOptionPane.showMessageDialog(this, "Perda cadastrada com sucesso!", "NotificaÃ§Ã£o", JOptionPane.WARNING_MESSAGE);
                buscaCodigoMaximo();
                limpaTela();
                habilitarDesabilitarCampos(false);
                btnNovo.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar a perda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            modelPerdas.setIdCodigo(Integer.parseInt(fieldCodigo.getText()));
            if (controllerPerdas.atualizarPerdasController(modelPerdas)) {
                JOptionPane.showMessageDialog(this, "Perda alterada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                limpaTela();
                //habilitarDesabilitarCampos(false);
                btnNovo.setEnabled(true);
                salvarAlterar = "alterar";
                dispose();
                if (this.todos == true) {
                    viewConsulta.carregarTodos();
                } else {
                    viewConsulta.carregarPerdas(!todos);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar a perda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }

    }

    public void buscaCodigoMaximo() {
        DAOPerdas daoPerdas = new DAOPerdas();
        c.conectar();
        try {
            cod = daoPerdas.buscaCodigoMaximo();
        } catch (SQLException ex) {
            Logger.getLogger(ViewEtapas.class.getName()).log(Level.SEVERE, null, ex);
        }
        fieldCodigo.setText(String.valueOf(cod + 1));
    }

