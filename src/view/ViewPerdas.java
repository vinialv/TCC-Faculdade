    public void textonulo() {
        Point p = this.getLocation();
        ViewPerdas telaPerdas = this;
        new Thread() {
            @Override
            public void run() {
                try {
                    for (int i = 0; i < 4; i++) {
                        telaPerdas.setLocation(p.x - 10, p.y - 2);
                        sleep(20);
                        telaPerdas.setLocation(p.x + 10, p.y + 2);
                        sleep(20);
                    }
                    telaPerdas.setLocation(p.x, p.y);
                } catch (InterruptedException ex) {
                    Logger.getLogger(ViewLogin.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }.start();
    }

    public void validacao() {
        if (comboProduto.getSelectedIndex() == -1 || comboEtapa.getSelectedIndex() == -1 || comboRecurso.getSelectedIndex() == -1
                || dataPerdas.getText().equals("") || horaPerdas.getText().equals("") || fieldPeso.getText().equals("") 
                || comboMotivo.getSelectedIndex() == -1 || comboOperador.getSelectedIndex() == -1) {
            textonulo();
            JOptionPane.showMessageDialog(this, "Preencha todos os campos para realizar o cadastro.");
        } else {
            salvar();
        }
    }


