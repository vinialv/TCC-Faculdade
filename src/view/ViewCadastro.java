    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        salvarAlterar = "altear";
        modelCadastroGeral = new ModelCadastroGeral();
        int linha = listaCadastros.getSelectedRow();
        int codigo = 0;
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um produto na tabela, antes de clicar em alterar!");
        } else {
            codigo = (int) listaCadastros.getValueAt(linha, 0);
            modelCadastroGeral = controllerCadastroGeral.retornarCadastroGeralController(codigo);
        }

        fieldCodigo.setText(String.valueOf(modelCadastroGeral.getIdCadastro()));
        jComboBox1.setSelectedItem(modelCadastroGeral.getTipoCadastro());
        fieldDescricao.setText(String.valueOf(modelCadastroGeral.getDescricao()));

        habilitarDesabilitarCampos(true);

    }//GEN-LAST:event_btnAlterarActionPerformed

