    //CODIGOS
    public void salvar() {

        if (jComboBox1.getSelectedIndex() == -1 || fieldDescricao.getText().equals("")) {
            textonulo();
            JOptionPane.showMessageDialog(this, "Preencha todas informações para concluir o cadastro!");
            return;
        }

        modelCadastroGeral = new ModelCadastroGeral();
        modelCadastroGeral.setTipoCadastro(jComboBox1.getSelectedItem().toString());
        modelCadastroGeral.setDescricao(fieldDescricao.getText());

        if (salvarAlterar.equals("salvar")) {
            if (controllerCadastroGeral.salvarCadastroGeralController(modelCadastroGeral) > 0) {
                JOptionPane.showMessageDialog(this, "Cadastro cadastro com sucesso!", "Notificação", JOptionPane.WARNING_MESSAGE);
                limparCampos();
                habilitarDesabilitarCampos(false);
                salvarAlterar = "salvar";
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar o cadastro!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            modelCadastroGeral.setIdCadastro(Integer.parseInt(fieldCodigo.getText()));
            if (controllerCadastroGeral.atualizarCadastroGeralController(modelCadastroGeral)) {
                JOptionPane.showMessageDialog(this, "Cadastro alterado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
                carregarCadastros();
                salvarAlterar = "salvar";
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar o cadastro!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void carregarCadastros() {
        listaModelCadastroGeral = controllerCadastroGeral.retornarListaParadasController();

        DefaultTableModel modelo = (DefaultTableModel) listaCadastros.getModel();
        modelo.setNumRows(0);
        //inserir produtos na tabela
        int cont = listaModelCadastroGeral.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelCadastroGeral.get(i).getIdCadastro(),
                listaModelCadastroGeral.get(i).getTipoCadastro(),
                listaModelCadastroGeral.get(i).getDescricao()
            });
        }
    }

    public void buscaCodigoMaximo() {
        DAOCadastroGeral daoCadastroGeral = new DAOCadastroGeral();
        c.conectar();
        try {
            cod = daoCadastroGeral.buscaCodigoMaximo();
        } catch (SQLException ex) {
            Logger.getLogger(ViewEtapas.class.getName()).log(Level.SEVERE, null, ex);
        }
        fieldCodigo.setText(String.valueOf(cod + 1));
    }

    public void limparCampos() {
        fieldCodigo.setText("");
        jComboBox1.setSelectedIndex(-1);
        fieldDescricao.setText("");
    }

