    public void textonulo() {
        Point p = this.getLocation();
        ViewCadastro telaCadastro = this;
        new Thread() {
            @Override
            public void run() {
                try {
                    for (int i = 0; i < 4; i++) {
                        telaCadastro.setLocation(p.x - 10, p.y - 2);
                        sleep(20);
                        telaCadastro.setLocation(p.x + 10, p.y + 2);
                        sleep(20);
                    }
                    telaCadastro.setLocation(p.x, p.y);
                } catch (InterruptedException ex) {
                    Logger.getLogger(ViewLogin.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }.start();
    }

